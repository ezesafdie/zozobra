void dibujarTextoLetra2(uint16_t x, uint16_t y, const char *texto, uint8_t colorIndice, uint8_t espaciado)
{
    uint16_t cursor_x = x;

    while (*texto != '\0')
    {
        t_letra letra = obtenerLetra2(*texto);

        // Si encontramos una letra válida, la dibujamos
        if (letra.matriz != NULL)
        {
            dibujarLetra2(cursor_x, y, letra.matriz, letra.ancho, colorIndice);
        }

        // Avanzamos el cursor según el ancho de la letra + el espaciado
        // Si es un espacio (' '), letra.matriz será NULL pero letra.ancho será 6.
        cursor_x += letra.ancho + espaciado;

        // Pasamos al siguiente carácter del string
        texto++;

    }
}


